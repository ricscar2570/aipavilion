#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "../..");
const sourcePath = path.join(root, "template.yaml");
const targetPath = path.join(root, "infrastructure", "backend-pilot.yaml");

function transform(source) {
    let output = source
        .replace(
            "Description: AI Pavilion Phase 4 controlled-pilot development foundation",
            "Description: AI Pavilion Phase 4 persistent staging and production-pilot backend",
        )
        .replace(
            "    Default: dev\n    AllowedValues: [dev]",
            "    Default: staging\n    AllowedValues: [staging, production]",
        )
        .replace(
            "    Default: http://127.0.0.1:3000",
            "    Default: https://staging.example.invalid",
        )
        .replace(
            "    AllowedValues: [disabled, simulated, stripe]\n    Description: Use simulated only for disposable development stacks.",
            "    AllowedValues: [disabled, stripe]\n    Description: Persistent pilot stacks prohibit simulated customer payments.",
        )
        .replace(
            "  BillingMode:\n    Type: String\n    Default: disabled\n    AllowedValues: [disabled, simulated, stripe]",
            "  BillingMode:\n    Type: String\n    Default: disabled\n    AllowedValues: [disabled, stripe]",
        )
        .replace(
            "  InvitationEmailMode:\n    Type: String\n    Default: simulated\n    AllowedValues: [disabled, simulated, ses]",
            "  InvitationEmailMode:\n    Type: String\n    Default: ses\n    AllowedValues: [disabled, ses]",
        )
        .replace(
            "  BotChallengeMode:\n    Type: String\n    Default: simulated\n    AllowedValues: [disabled, simulated, turnstile]",
            "  BotChallengeMode:\n    Type: String\n    Default: turnstile\n    AllowedValues: [disabled, turnstile]",
        )
        .replace(/DeletionPolicy: Delete/g, "DeletionPolicy: Retain")
        .replace(/UpdateReplacePolicy: Delete/g, "UpdateReplacePolicy: Retain")
        .replace(
            /PointInTimeRecoveryEnabled: false/g,
            "PointInTimeRecoveryEnabled: true",
        )
        .replace(
            '      MfaConfiguration: "OFF"',
            "      MfaConfiguration: OPTIONAL",
        )
        .replace(
            "      DeletionProtection: INACTIVE",
            "      DeletionProtection: ACTIVE",
        )
        .replace(/RetentionInDays: 7/g, "RetentionInDays: 90")
        .replace(/RetentionInDays: 14/g, "RetentionInDays: 90")
        .replace("Stripe test-mode credentials", "Stripe pilot credentials")
        .replace(
            "ARN of the stack-managed Stripe test secret",
            "ARN of the stack-managed Stripe pilot secret",
        );
    return `# GENERATED by scripts/pilot/generate-pilot-backend.js. Do not edit directly.\n${output}`;
}

const expected = transform(fs.readFileSync(sourcePath, "utf8"));
if (process.argv.includes("--check")) {
    const actual = fs.existsSync(targetPath)
        ? fs.readFileSync(targetPath, "utf8")
        : "";
    if (actual !== expected) {
        console.error(
            "infrastructure/backend-pilot.yaml is stale. Run npm run pilot:generate.",
        );
        process.exit(1);
    }
    console.log("Pilot backend template is current.");
} else {
    fs.writeFileSync(targetPath, expected);
    console.log(`Wrote ${path.relative(root, targetPath)}`);
}
